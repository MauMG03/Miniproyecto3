public class Telefono {
    private int numero;
    private String tipo;
    
    public Telefono(){
        numero = 0;
        tipo = "";
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
